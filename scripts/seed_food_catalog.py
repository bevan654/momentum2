def seed_food_catalog():
    """Insert 1000 unique foods into food_catalog, skipping duplicates."""
    # Fetch existing names to avoid duplicates
    existing = set()
    offset = 0
    while True:
        res = supabase.table("food_catalog").select("name").range(offset, offset + 999).execute()
        if not res.data:
            break
        for row in res.data:
            existing.add(row["name"].lower())
        if len(res.data) < 1000:
            break
        offset += 1000

    print(f"Found {len(existing)} existing foods in catalog")

    to_insert = []
    for name, brand, cal, pro, carb, fat, fiber, sugar, serv_size, serv_unit, category in FOODS:
        if name.lower() in existing:
            continue
        to_insert.append({
            "name": name,
            "brand": brand,
            "calories": cal,
            "protein": pro,
            "carbs": carb,
            "fat": fat,
            "fiber": fiber,
            "sugar": sugar,
            "serving_size": serv_size,
            "serving_unit": serv_unit,
            "confidence": "verified",
            "popularity": 0,
            "category": category,
        })

    if not to_insert:
        print("All foods already exist. Nothing to insert.")
        return

    # Insert in batches of 100
    total = len(to_insert)
    inserted = 0
    batch_size = 100
    for i in range(0, total, batch_size):
        batch = to_insert[i : i + batch_size]
        res = supabase.table("food_catalog").insert(batch).execute()
        inserted += len(res.data) if res.data else 0
        print(f"  Inserted batch {i // batch_size + 1}: {len(batch)} rows")

    print(f"\nDone! Inserted {inserted} new foods into food_catalog.")
    print(f"Total unique foods in list: {len(FOODS)}")


if __name__ == "__main__":
    seed_food_catalog()
